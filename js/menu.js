function menuOpen( objName ) {
	document.getElementById( objName ).style.height='auto';
	document.getElementById( objName ).style.color='#eeeeee';
	document.getElementById( objName ).style.fontWeight='bold';
	document.getElementById( objName ).style.border='2px solid #111111';
}

function menuClose( objName ) {
	document.getElementById( objName ).style.height='17px';
	document.getElementById( objName ).style.color='#111111';
	document.getElementById( objName ).style.fontWeight='normal';
	document.getElementById( objName ).style.border='2px solid #eeeeee';
}
